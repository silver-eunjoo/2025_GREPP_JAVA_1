package day4.class_and_abstraction;

public class Vegetable {
    private int carbs;
    private int protein;

    private int fat;

    private int vitaminC;
    private int vitaminD;
    private int vitaminE;

    public void beHealthy(int carbs, int protein, int fat, int vitaminC, int vitaminD, int vitaminE){
        this.carbs = carbs;
        this.protein = protein;
        this.fat = fat;
        this.vitaminC = vitaminC;
        this.vitaminD = vitaminD;
        this.vitaminE = vitaminE;
    }


}
